    public void addRowToJTable(List<Pais> paises)
    {
        ArrayList<Pais> list = (ArrayList<Pais>)paises;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[4];
        for( int i = model.getRowCount() - 1; i >= 0; i-- )
        {
             model.removeRow(i);
        }
        for(int i = 0; i < list.size(); i++)
        {   

            rowData[0] = list.get(i).getNombre();
            rowData[1] = list.get(i).getPoblacion();
            rowData[2] = list.get(i).getContagiadosCount();
            rowData[3] = list.get(i).getMuertosCount();
            model.addRow(rowData);           
            
                   
        }

                
    }
    
